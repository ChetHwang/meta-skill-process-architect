def slugify(text):
    """Convert text to kebab-case slug."""
    text = text.lower().strip()
    text = re.sub(r'[^\w\s-]', '', text)
    text = re.sub(r'[\s_]+', '-', text)
    return text


# ──────────────────────────────────────────────
# Command: init
# ──────────────────────────────────────────────

def cmd_init(args):
    """Initialize a new process analysis session."""
    name = args.name
    materials_path = Path(args.materials).resolve() if args.materials else None
    output_path = Path(args.output or f"process-{slugify(name)}").resolve()

    if output_path.exists():
        print(f"\u26a0\ufe0f  Output directory '{output_path}' already exists.")
        if not args.force:
            print("   Use --force to overwrite.")
            return

    print(f"\U0001f50d Initializing analysis for: {name}")
    if materials_path:
        print(f"\U0001f4c1 Materials: {materials_path}")
    print(f"\U0001f4e6 Output: {output_path}")

    # Create output structure
    dirs = [
        output_path,
        output_path / "references",
        output_path / "agents",
        output_path / "assets",
        output_path / "assets" / "flowcharts",
        output_path / "assets" / "forms",
        output_path / "assets" / "templates",
    ]
    for d in dirs:
        d.mkdir(parents=True, exist_ok=True)

    # Write manifest
    manifest = {
        "process_name": name,
        "created_at": datetime.now().isoformat(),
        "materials_path": str(materials_path) if materials_path else None,
        "status": "initialized",
    }
    with open(output_path / "manifest.json", "w") as f:
        json.dump(manifest, f, indent=2, ensure_ascii=False)

    print(f"\u2705 Initialized: {output_path}")
    print(f"   Next: add materials to {materials_path or output_path} and run 'generate'")


