def cmd_generate(args):
    """Generate process Skill from materials."""
    output_path = Path(args.output).resolve()

    if not output_path.exists():
        print(f"\u274c Output directory '{output_path}' does not exist. Run 'init' first.")
        return

    manifest_path = output_path / "manifest.json"
    if manifest_path.exists():
        with open(manifest_path) as f:
            manifest = json.load(f)
    else:
        manifest = {"process_name": output_path.name}

    print(f"\U0001f527 Generating process Skill...")
    print(f"   Output: {output_path}")

    # Phase 1: Generate placeholder files with template structure
    # In production, this would be driven by the agent workflow (SKILL.md)
    # The CLI provides scaffolding; content generation is agent-driven.

    files_created = []

    # Write SKILL.md placeholder if not exists
    skill_md = output_path / "SKILL.md"
    if not skill_md.exists():
        skill_md.write_text(f"---\n"
                           f"name: {manifest.get('process_name', 'process')}\n"
                           f"generated_by_meta_skill_version: {VERSION}\n"
                           f"---\n\n"
                           f"# {manifest.get('process_name', 'Process')}\n\n"
                           f"> Generated by meta-skill-process-architect v{VERSION}\n"
                           f"> Date: {datetime.now().strftime('%Y-%m-%d')}\n\n"
                           f"## Overview\n\n_TODO: Run agent-driven workflow to fill content._\n\n"
                           f"See `references/process-brief.md` for full process definition.\n")
        files_created.append("SKILL.md")

    # Write VERSION (with temporal validity fields)
    version_file = output_path / "VERSION"
    if not version_file.exists():
        version_file.write_text("1.0.0\n"
                               "valid_from: \n"
                               "valid_until: \n"
                               "lifecycle_phase: draft\n"
                               "trial_until: \n")
        files_created.append("VERSION")

    # Write placeholder reference files
    ref_dir = output_path / "references"
    ref_files = {
        "process-brief.md": "# Process Brief\n\n_TODO: Fill L1.1-L1.9 sub-sections._\n",
        "foundational-logic.md": "# Foundational Logic\n\n_TODO: Fill L2 sub-sections._\n",
        "output-recipes.md": "# Output Recipes\n\n_TODO: Fill L3 recipes._\n",
        "artifacts-registry.md": "# Artifacts Registry\n\n_TODO: Fill L4 artifacts._\n",
        "visualization-spec.md": "# Visualization Spec\n\n_TODO: Fill L5 visualization conventions._\n",
        "cases-library.md": "# Cases & Learning Library\n\n_TODO: Fill L6 cases._\n",
    }
    for fname, content in ref_files.items():
        fpath = ref_dir / fname
        if not fpath.exists():
            fpath.write_text(content)
            files_created.append(f"references/{fname}")

    # Update manifest
    manifest["status"] = "generated"
    manifest["generated_at"] = datetime.now().isoformat()
    with open(manifest_path, "w") as f:
        json.dump(manifest, f, indent=2, ensure_ascii=False)

    if files_created:
        print(f"\u2705 Generated {len(files_created)} files:")
        for f in files_created:
            print(f"   - {f}")
    else:
        print("\u2139\ufe0f  All files already exist. Use --force to regenerate.")

    print(f"   Next: run 'validate' to check quality, then 'package' to bundle.")


