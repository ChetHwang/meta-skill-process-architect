    init_parser.add_argument("--output", help="Output directory (default: process-{slug})")
    init_parser.add_argument("--force", action="store_true", help="Overwrite existing output directory")

    # generate
    gen_parser = subparsers.add_parser("generate", help="Generate process Skill from materials")
    gen_parser.add_argument("--output", required=True, help="Output directory")
    gen_parser.add_argument("--force", action="store_true", help="Regenerate existing files")

