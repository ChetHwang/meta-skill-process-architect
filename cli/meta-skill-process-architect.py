    pkg_parser = subparsers.add_parser("package", help="Package a process Skill for distribution")
    pkg_parser.add_argument("--skill", required=True, help="Path to process Skill directory")
    pkg_parser.add_argument("--format", default="workbuddy",
                           choices=["workbuddy", "openclaw", "claude-code", "standalone"],
                           help="Target platform format")

