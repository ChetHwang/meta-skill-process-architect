def cmd_which_version(args):
    """Query which process version was active on a given date."""
    from datetime import date as date_type

    skill_path = Path(args.skill).resolve()
    changelog_file = skill_path / "CHANGELOG.md"
    version_file = skill_path / "VERSION"

    if args.list:
        # List all versions with temporal windows
        if not changelog_file.exists():
            print(f"\u26a0\ufe0f  No CHANGELOG found in {skill_path}")
            return

        content = changelog_file.read_text()
        versions = []
        for line in content.split("\n"):
            if line.startswith("## [") and " — " in line:
                ver_part = line.split("## [")[1].split("]")[0]
                rest = line.split("] — ")[1] if "] — " in line else ""
                versions.append(f"  v{ver_part}  {rest}")

        if versions:
            print(f"\U0001f4c5 Versions for {skill_path.name}:")
            for v in versions:
                print(v)
        else:
            print("\u2139\ufe0f  No version entries found in CHANGELOG.")
        return

    if not args.at:
        # If no date specified and no --list, show current version info
        if version_file.exists():
            content = version_file.read_text()
            print(f"\U0001f4c5 {skill_path.name}:")
            print(content)
        else:
            print(f"\u26a0\ufe0f  No VERSION file found in {skill_path}")
        return

    # Parse query date
    try:
        query_date = date_type.fromisoformat(args.at)
    except ValueError:
        print(f"\u274c Invalid date format: {args.at}. Use YYYY-MM-DD.")
        return

    # Scan VERSION file for temporal info
    if version_file.exists():
        v_content = version_file.read_text()
        phase = "unknown"
        for line in v_content.split("\n"):
            if line.startswith("lifecycle_phase:"):
                phase = line.split(":", 1)[1].strip()

        version_num = v_content.split("\n")[0].strip()

        if args.phase:
            print(f"v{version_num}: {phase}")
            if phase == "trial":
                for line in v_content.split("\n"):
                    if line.startswith("trial_until:") and line.split(":", 1)[1].strip():
                        print(f"  trial until: {line.split(':', 1)[1].strip()}")
            return

        # Scan CHANGELOG for temporal windows
        if changelog_file.exists():
            content = changelog_file.read_text()
            best_match = None
            for line in content.split("\n"):
                if line.startswith("## [") and " — " in line:
                    # Parse: ## [X.Y.Z] — YYYY-MM-DD (phase, trial until YYYY-MM-DD)
                    try:
                        ver_part = line.split("## [")[1].split("]")[0]
                        date_part = line.split("] — ")[1].split(" ")[0] if "] — " in line else None
                        if date_part:
                            entry_date = date_type.fromisoformat(date_part)
                            if entry_date <= query_date:
                                if best_match is None or date_type.fromisoformat(best_match[1]) < entry_date:
                                    best_match = (ver_part, date_part, line)
                    except (ValueError, IndexError):
                        continue

            if best_match:
                print(f"\U0001f4c5 At {args.at}, authoritative version: v{best_match[0]}")
                print(f"   (effective from {best_match[1]})")
                print(f"   {best_match[2]}")
                return

    # Fallback: just show current version
    print(f"\U0001f4c5 {skill_path.name}: v{version_num} ({phase})")
    print(f"   (no temporal window info available for date {args.at})")



