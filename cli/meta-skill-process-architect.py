def cmd_package(args):
    """Package a process Skill for distribution."""
    skill_path = Path(args.skill).resolve()
    fmt = args.format  # workbuddy, openclaw, claude-code, standalone

    if not skill_path.exists():
        print(f"\u274c Skill path '{skill_path}' does not exist.")
        return

    print(f"\U0001f4e6 Packaging: {skill_path}")
    print(f"   Format: {fmt}")

    # Exclude patterns that should never be in a distribution package
    EXCLUDE_DIRS = {".git", ".github", ".workbuddy", "__pycache__"}
    EXCLUDE_FILES = {".DS_Store", ".gitignore"}

    def _should_exclude(name):
        """Check if a file/dir should be excluded from packaging."""
        basename = name.split("/")[-1] if "/" in name else name
        return basename in EXCLUDE_DIRS or basename in EXCLUDE_FILES or basename.startswith("._") or basename.endswith(".pyc")

    if fmt == "workbuddy" or fmt == "openclaw":
        # Create tar.gz for WorkBuddy/OpenClaw import
        output_file = skill_path.parent / f"{skill_path.name}.tar.gz"
        import tarfile
        with tarfile.open(output_file, "w:gz") as tar:
            for item in skill_path.rglob("*"):
                # Skip excluded patterns
                parts = item.relative_to(skill_path).parts
                if any(_should_exclude(p) for p in parts):
                    continue
                tar.add(item, arcname=str(item.relative_to(skill_path.parent)))
        print(f"\u2705 Packaged: {output_file}")
        if fmt == "workbuddy":
            print(f"   Import via WorkBuddy: Skills → Import → {output_file.name}")
        else:
            print(f"   Import via OpenClaw: skills install {output_file}")


