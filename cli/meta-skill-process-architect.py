    bump_parser = subparsers.add_parser("bump", help="Bump version and update CHANGELOG")
    bump_parser.add_argument("--skill", required=True, help="Path to process Skill directory")
    bump_parser.add_argument("--type", required=True, choices=["major", "minor", "patch"],
                           help="Bump type (MAJOR/MINOR/PATCH)")
    bump_parser.add_argument("message", help="Changelog message describing the change")
    bump_parser.add_argument("--dry-run", action="store_true", help="Preview changes without applying")
    bump_parser.add_argument("--phase", choices=["trial", "active"], help="Set lifecycle phase on bump")
    bump_parser.add_argument("--valid-from", help="Effective date for this version (YYYY-MM-DD)")
    bump_parser.add_argument("--trial-until", help="Trial period end date (YYYY-MM-DD, only when --phase trial)")

