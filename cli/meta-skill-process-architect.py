def _ap006_check(skill):
    """Check for critical steps without exception paths — steps with invariants but no .E/.A sibling.
    
    Phase 3: Full implementation. Parses L1.4 step IDs, identifies critical steps by keywords,
    checks for corresponding .E/.A exception paths.
    """
    issues = []
    brief_content = skill.get("references/process-brief.md", "")
    if not brief_content:
        return issues

    # Extract all step IDs from L1.4
    step_ids = set()
    for match in re.finditer(r'(?:###|####)\s+(S\d+(?:\.\d+)?(?:\.E|\.A)?)', brief_content):
        step_ids.add(match.group(1))

    # Critical keywords: steps that likely need exception paths
    CRITICAL_KEYWORDS = [
        "审批", "退款", "支付", "扣款", "修改", "删除", "变更",
        "approve", "refund", "payment", "delete", "modify",
        "学籍", "订单", "发货", "入库",
    ]
    # Non-critical step markers: simple notifications don't need exception paths
    NON_CRITICAL_MARKERS = ["通知", "确认", "记录", "归档", "notify", "confirm", "log"]

    # Find main step IDs (S1, S2, etc. without .E/.A/.N suffixes)
    main_steps = [sid for sid in step_ids if re.match(r'^S\d+$', sid)]

    for step_id in sorted(main_steps):
        # Check if this step has an exception path
        has_exception = f"{step_id}.E" in step_ids or f"{step_id}.A" in step_ids
        
        if not has_exception:
            # Find the step's content to determine if it's critical
            step_section_pattern = rf'(?:###|####)\s+{re.escape(step_id)}\s*[-—]\s*(.+?)(?=\n(?:###|####|\Z))'
            step_match = re.search(step_section_pattern, brief_content, re.DOTALL)
            if step_match:
                step_body = step_match.group(1)
                is_critical = any(kw in step_body for kw in CRITICAL_KEYWORDS)
                is_non_critical = any(kw in step_body for kw in NON_CRITICAL_MARKERS)
                
                if is_critical and not is_non_critical:
                    issues.append(
                        f"references/process-brief.md: {step_id} has critical keywords but no .E/.A exception path"
                    )
    
    return issues
def _ap007_check(skill):
    """Check for undocumented decisions >= 50%."""
    all_text = " ".join(skill.values())
    total = len(re.findall(r'condition_quality', all_text, re.IGNORECASE))
    undocumented = len(re.findall(r'condition_quality[:\s]*undocumented', all_text, re.IGNORECASE))
    if total > 0 and undocumented / total >= 0.5:
        return [f"{undocumented}/{total} decisions are undocumented (AP-007)"]
    return []


def _ap008_check(skill):
    """Check for no-information trigger."""
    issues = []
    for fname, content in skill.items():
        # Look for trigger.source patterns
        match = re.search(r'(?:trigger.*?source|触发.*?来源)[：:\s]*["\']?(.+?)["\']?(?:\n|$)', content, re.IGNORECASE | re.DOTALL)
        if match:
            trigger_val = match.group(1).strip().lower()
            for bad in NO_INFO_TRIGGERS:
                if trigger_val == bad:
                    issues.append(f"{fname}: trigger is no-information '{trigger_val}' (AP-008)")
                    break
    return issues


