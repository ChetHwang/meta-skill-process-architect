def cmd_meta_self_check(args):
    """
    Automated cross-file version consistency check.
    Enforces SELF-VALIDATION G10 (5-file version consistency) and
    G11 (examples generated_by_meta_skill_version matches current).
    Exits 1 on any mismatch.
    """
    import re as _re

    root = Path(args.root or ".").resolve()
    # Walk up if VERSION not in cwd
    while not (root / "VERSION").exists() and root.parent != root:
        root = root.parent
    if not (root / "VERSION").exists():
        print("❌ No VERSION file found ascending from cwd.")
        sys.exit(2)

    print(f"\U0001f50d Meta self-check from: {root}\n")

    target = (root / "VERSION").read_text().strip()
    sources = {"VERSION file": target}

    # CLI internal VERSION
    cli_path = root / "cli" / "meta-skill-process-architect.py"
    if cli_path.exists():
        m = _re.search(r'^VERSION\s*=\s*"([^"]+)"', cli_path.read_text(), _re.MULTILINE)
        sources["cli/...py VERSION"] = m.group(1) if m else "MISSING"
    else:
        sources["cli/...py VERSION"] = "NO FILE"

    # README badge
    readme = root / "README.md"
    if readme.exists():
        m = _re.search(r"version-([\d.]+)-blue", readme.read_text())
        sources["README badge"] = m.group(1) if m else "MISSING"
    else:
        sources["README badge"] = "NO FILE"

    # SELF-VALIDATION title
    sv = root / "SELF-VALIDATION.md"
    if sv.exists():
        first_line = sv.read_text().split("\n", 1)[0]
        m = _re.search(r"v(\d+\.\d+\.\d+)", first_line)
        sources["SELF-VALIDATION title"] = m.group(1) if m else "MISSING"
    else:
        sources["SELF-VALIDATION title"] = "NO FILE"

    # CHANGELOG latest entry
    cl = root / "CHANGELOG.md"
    if cl.exists():
        m = _re.search(r"^## \[([\d.]+)\]", cl.read_text(), _re.MULTILINE)
        sources["CHANGELOG latest"] = m.group(1) if m else "MISSING"
    else:
        sources["CHANGELOG latest"] = "NO FILE"

    print("G10 — 5-file version consistency:")
    g10_pass = True
    for name, value in sources.items():
        ok = (value == target)
        icon = "✅" if ok else "❌"
        print(f"  {icon} {name}: {value}")
        if not ok:
            g10_pass = False

    # G11: examples consistency
    print("\nG11 — examples generated_by_meta_skill_version:")
    g11_pass = True
    examples_dir = root / "examples"
    if examples_dir.exists():
        ex_skills = sorted(examples_dir.glob("*/SKILL.md"))
        if not ex_skills:
            print(f"  ⚠️  no examples/*/SKILL.md found")
        for ex in ex_skills:
            content = ex.read_text()
            m = _re.search(r"generated_by_meta_skill_version:\s*([\d.]+)", content)
            ex_ver = m.group(1) if m else "MISSING"
            ok = (ex_ver == target)
            icon = "✅" if ok else "❌"
            print(f"  {icon} {ex.parent.name}: {ex_ver}")
            if not ok:
                g11_pass = False
    else:
        print("  ⚠️  no examples/ directory")

    # G12: examples satisfy archetype-prescribed cross-cutting requirements
    # As archetype evolves, add new patterns here.
    # Each pattern: (requirement_id, requirement_name, file_glob_within_example, regex_pattern)
    archetype_requirements = [
        ("C5", "Cycle Time annotation (plan §3 C5)",
         "references/process-brief.md", r"Cycle Time:|fastest=|bottleneck="),
        ("C6", "Internal Handoff Summary (plan §3 C6)",
         "references/process-brief.md", r"Internal Handoff Summary|内部.*交接|Internal Handoff"),
    ]

    print("\nG12 — examples satisfy archetype cross-cutting requirements:")
    g12_pass = True
    if examples_dir.exists():
        ex_dirs = sorted([d for d in examples_dir.iterdir() if d.is_dir()])
        for ex_dir in ex_dirs:
            for req_id, req_name, rel_path, pattern in archetype_requirements:
                target_file = ex_dir / rel_path
                if not target_file.exists():
                    print(f"  ❌ {ex_dir.name} / {req_id}: {rel_path} not found")
                    g12_pass = False
                    continue
                content = target_file.read_text()
                if _re.search(pattern, content):
                    print(f"  ✅ {ex_dir.name} / {req_id} ({req_name.split(' (')[0]})")
                else:
                    print(f"  ❌ {ex_dir.name} / {req_id}: missing '{req_name}'")
                    g12_pass = False

    print()
    if g10_pass and g11_pass and g12_pass:
        print(f"✅ PASS — all G10/G11/G12 checks consistent at {target}")
        sys.exit(0)
    else:
        print(f"❌ FAIL — meta-skill self-consistency violated. Expected version: {target}")
        if not (g10_pass and g11_pass):
            print("\nG10/G11 fix:")
            print("  1. Bump source-of-truth (VERSION file)")
            print("  2. Sync the others: README badge, SELF-VAL title, CLI VERSION, CHANGELOG latest")
            print("  3. Update each examples/*/SKILL.md frontmatter generated_by_meta_skill_version")
        if not g12_pass:
            print("\nG12 fix:")
            print("  1. For each failing example, open references/process-brief.md")
            print("  2. Add the missing section (Cycle Time annotation or Internal Handoff Summary)")
            print("  3. Or update archetype if the requirement should no longer apply")
        print("\nThen re-run: process-architect meta-self-check")
        sys.exit(1)


