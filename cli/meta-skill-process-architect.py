def cmd_impact(args):
    """Run impact analysis for a process change."""
    skill_path = Path(args.skill).resolve() if args.skill else None
    skills_dir = Path(args.skills_dir).resolve() if hasattr(args, 'skills_dir') and args.skills_dir else None
    change_type = args.change_type

    print("=" * 60)
    print("Impact Analysis")
    print("=" * 60)

    # Direct impact analysis
    if skill_path and skill_path.exists():
        print(f"\n\U0001f50d Process: {skill_path.name}")
        
        brief_path = skill_path / "references" / "process-brief.md"
        if not brief_path.exists():
            print(f"  \u26a0\ufe0f  No process-brief.md found — skipping direct impact analysis")
        else:
            brief = brief_path.read_text()
            direct_impacts = []
            
            if change_type == "sla":
                step = getattr(args, 'step', None)
                old_val = getattr(args, 'old', None)
                new_val = getattr(args, 'new', None)
                print(f"  Change: SLA {step or 'overall'} — {old_val} → {new_val}")
                
                # Find the step
                if step:
                    step_pattern = re.compile(
                        rf'(?:###|####)\s+{re.escape(step)}\s*[-—].*?(?=\n(?:###|####|\Z))',
                        re.DOTALL
                    )
                    step_match = step_pattern.search(brief)
                    if step_match:
                        direct_impacts.append(f"L1.4 {step}: duration shortened from {old_val} to {new_val}")
                        # Check for exception paths
                        has_e = f"{step}.E" in brief
                        has_a = f"{step}.A" in brief
                        if has_e:
                            direct_impacts.append(f"  → {step}.E exception path SLA may need recalibration")
                        if has_a:
                            direct_impacts.append(f"  → {step}.A alternate path SLA may need recalibration")
                    else:
                        print(f"  \u26a0\ufe0f  Step {step} not found in process-brief")
                
                # Cycle time impact
                dur_matches = re.findall(
                    r'(?:时长|duration)[：:\s]*.*?(\d+)\s*(?:小时|hour|h|天|day)',
                    brief, re.IGNORECASE
                )
                if dur_matches:
                    print(f"  \U0001f4ca Cycle time: {len(dur_matches)} duration values found (recalculate total)")

            elif change_type == "role":
                old_name = getattr(args, 'old_name', None)
                new_name = getattr(args, 'new_name', None)
                print(f"  Change: Role rename — {old_name} → {new_name}")
                
                # Find all occurrences
                mentions = brief.count(old_name) if old_name else 0
                if mentions > 0:
                    direct_impacts.append(f"Role '{old_name}' appears {mentions} times in process-brief.md")
                    direct_impacts.append(f"  → All L1.4 steps referencing this role need executor_role update")
                    direct_impacts.append(f"  → RACI matrix (L1.7) needs role name update")
                    direct_impacts.append(f"  → All generated recipes must be regenerated with new role name")

            elif change_type == "tool":
                old_url = getattr(args, 'old_url', None)
                new_url = getattr(args, 'new_url', None)
                print(f"  Change: Tool URL — {old_url} → {new_url}")
                
                mentions = brief.count(old_url) if old_url else 0
                if mentions > 0:
                    direct_impacts.append(f"Tool URL appears {mentions} times in process-brief.md")
                    direct_impacts.append(f"  → Update L1.4 step descriptions and L1.2 input entries")
                else:
                    print(f"  \u26a0\ufe0f  Old URL not found in process-brief.md — no direct impacts")

            elif change_type == "auto":
                print(f"  Auto-detecting changes from baseline...")
                since = getattr(args, 'since', None)
                if since:
                    direct_impacts.append(f"Comparing current version against baseline: {since}")
                    direct_impacts.append(f"  → Run full diff-plan for detailed change list")
                    # Reuse diff-plan logic
                    steps = re.findall(r'(?:###|####)\s+S\d+', brief)
                    roles = re.findall(
                        r'[-*]\s+\*\*([\u4e00-\u9fff]{2,8}(?:班主任|负责人|老师|主管|财务|客服|质检|销售|教务))\*\*',
                        brief
                    )
                    sla_vals = re.findall(
                        r'(?:SLA|sla)[：:\s]*["\']?(\d+)\s*(?:小时|hour|h|天|day)',
                        brief, re.IGNORECASE
                    )
                    direct_impacts.append(f"  Detected: {len(steps)} steps, {len(set(roles))} roles, {len(sla_vals)} SLA values")

            # Print direct impacts
            if direct_impacts:
                print(f"\n  Direct Impacts ({len(direct_impacts)}):")
                for imp in direct_impacts:
                    print(f"    • {imp}")
            else:
                print(f"\n  ✅ No direct impacts detected on this process")

            # Suggested bump
            bump_map = {"sla": "MINOR", "role": "MINOR", "tool": "PATCH", "step": "MINOR", "step-remove": "MAJOR", "regulatory": "MAJOR", "auto": "PATCH"}
            suggested = bump_map.get(change_type, "PATCH")
            print(f"\n  Suggested bump: {suggested}")
            print(f"    process-architect bump --skill {skill_path} --type {suggested.lower()} \"{change_type} change: {getattr(args, 'old', getattr(args, 'old_name', getattr(args, 'old_url', 'auto-detected')))}\"")

    # Cross-process impact (if skills-dir provided)
    if skills_dir and skills_dir.exists():
        print(f"\n  \U0001f310 Cross-Process Impact:")
        process_dirs = sorted([d for d in skills_dir.iterdir()
                              if d.is_dir() and d.name.startswith("process-") and d.name != (skill_path.name if skill_path else "")])
        
        if not process_dirs:
            print(f"    No other process Skills found in {skills_dir}")
        else:
            downstream = []
            for pd in process_dirs:
                brief_p = pd / "references" / "process-brief.md"
                if not brief_p.exists():
                    continue
                content = brief_p.read_text()
                # Check if this process references our changed process
                skill_name = skill_path.name if skill_path else ""
                if skill_name and (skill_name in content or skill_path.name.replace("process-", "") in content):
                    # Find handoff references
                    handoff_matches = re.findall(
                        r'[-*]\s+\*\*(.+?)\*\*\s*\n.*?指向[：:]\s*(.+?)(?:\n|$)',
                        content, re.DOTALL
                    )
                    for desc, target in handoff_matches:
                        if skill_name in target or skill_path.name.replace("process-", "") in target:
                            downstream.append({
                                "process": pd.name,
                                "handoff_desc": desc,
                                "target": target,
                            })
            
            if downstream:
                print(f"    {len(downstream)} downstream process(es) affected:")
                for ds in downstream:
                    print(f"    \u26a0\ufe0f  {ds['process']} — handoff: {ds['handoff_desc']}")
                    print(f"       → Review {ds['process']}/references/process-brief.md L1.9")
                
                # SLA cascade check
                print(f"\n    SLA Cascade Check:")
                print(f"      ⚠️  Manual review required — verify downstream SLA deadlines are still achievable")
                if change_type == "sla":
                    print(f"      → If upstream changed, downstream handoff SLA margin may shrink")
            else:
                print(f"    ✅ No downstream processes reference this process")

    # Risk classification
    print(f"\n  Risk Classification:")
    if change_type in ("regulatory", "step-remove"):
        print(f"    \U0001f534 HIGH — {change_type} changes require careful review")
    elif change_type in ("role", "step"):
        print(f"    \U0001f7e1 MEDIUM — Review affected processes before proceeding")
    else:
        print(f"    \U0001f7e2 LOW — Isolated change, auto-approve with notification")

    # Output to file
    output_file = getattr(args, 'output', None)
    if output_file:
        import json as json_module
        report = {
            "change_type": change_type,
            "process": str(skill_path) if skill_path else None,
            "direct_impacts": direct_impacts if 'direct_impacts' in dir() else [],
            "suggested_bump": suggested,
            "generated_at": datetime.now().isoformat(),
        }
        with open(output_file, 'w') as f:
            json_module.dump(report, f, indent=2, ensure_ascii=False)
        print(f"\n  \u2705 Impact report saved: {output_file}")


