def _parse_version(version_str):
    """Parse 'X.Y.Z' into (int, int, int)."""
    parts = version_str.strip().split(".")
    if len(parts) != 3:
        raise ValueError(f"Invalid version format: {version_str}")
    return int(parts[0]), int(parts[1]), int(parts[2])


def _bump_version(current, bump_type):
    """Bump version according to semver."""
    major, minor, patch = _parse_version(current)
    if bump_type == "MAJOR":
        return f"{major + 1}.0.0"
    elif bump_type == "MINOR":
        return f"{major}.{minor + 1}.0"
    elif bump_type == "PATCH":
        return f"{major}.{minor}.{patch + 1}"
    else:
        raise ValueError(f"Unknown bump type: {bump_type}")


def _get_current_quarter():
    """Return current quarter as YYYY-Q#."""
    now = datetime.now()
    quarter = (now.month - 1) // 3 + 1
    return f"{now.year}-Q{quarter}"


def cmd_bump(args):
    """Bump version and update CHANGELOG for a process Skill."""
    skill_path = Path(args.skill).resolve()
    bump_type = args.type.upper()
    message = args.message

    if bump_type not in ("MAJOR", "MINOR", "PATCH"):
        print(f"\u274c Invalid bump type: {bump_type}. Use MAJOR, MINOR, or PATCH.")
        return

    version_file = skill_path / "VERSION"
    changelog_file = skill_path / "CHANGELOG.md"

    if not version_file.exists():
        print(f"\u274c VERSION file not found in {skill_path}")
        return

    current_version = version_file.read_text().strip()
    new_version = _bump_version(current_version, bump_type)

    print(f"\U0001f4e6 Bump: {current_version} → {new_version} ({bump_type})")
    print(f"   Message: {message}")
    print(f"   Skill: {skill_path}")

    if args.dry_run:
        print(f"\n   [DRY RUN] Would update:")
        print(f"     VERSION: {current_version} → {new_version}")
        print(f"     CHANGELOG.md: append [{new_version}] entry")
        return

    # Update VERSION
    version_file.write_text(new_version + "\n")
    print(f"   \u2705 VERSION updated: {new_version}")

    # Update CHANGELOG
    quarter = _get_current_quarter()
    date_str = datetime.now().strftime("%Y-%m-%d")
    new_entry = f"""## [{new_version}] — {quarter} ({date_str})

### Changed
- {message}

"""
    if changelog_file.exists():
        existing = changelog_file.read_text()
        # Insert after the first line (title) or at the top
        lines = existing.split("\n")
        if lines and lines[0].startswith("# "):
            changelog_file.write_text(lines[0] + "\n\n" + new_entry + "\n".join(lines[1:]).lstrip("\n"))
        else:
            changelog_file.write_text(new_entry + existing)
    else:
        changelog_file.write_text(f"# Changelog — {skill_path.name}\n\n" + new_entry)

    print(f"   \u2705 CHANGELOG updated")

    # Update temporal fields in VERSION if --phase provided
    if hasattr(args, 'phase') and args.phase:
        version_lines = version_file.read_text().strip().split("\n")
        new_lines = []
        valid_from = getattr(args, 'valid_from', None) or date_str
        trial_until = getattr(args, 'trial_until', None) or ""
        for line in version_lines:
            if line.startswith("valid_from:"):
                new_lines.append(f"valid_from: {valid_from}")
            elif line.startswith("valid_until:"):
                new_lines.append("valid_until: ")
            elif line.startswith("lifecycle_phase:"):
                new_lines.append(f"lifecycle_phase: {args.phase}")
            elif line.startswith("trial_until:"):
                new_lines.append(f"trial_until: {trial_until}" if args.phase == "trial" and trial_until else "trial_until: ")
            elif line and not line.startswith("valid_") and not line.startswith("lifecycle_") and not line.startswith("trial_"):
                new_lines.append(line)
        version_file.write_text("\n".join(new_lines) + "\n")
        print(f"   \u2705 VERSION temporal fields updated (phase={args.phase}, valid_from={valid_from})")

    # If previous version was active and this is a new version, mark old version as superseded
    if hasattr(args, 'phase') and args.phase and current_version != new_version:
        # Mark previous version's valid_until
        old_valid_until = valid_from if hasattr(args, 'valid_from') and args.valid_from else date_str
        print(f"   \u2139\ufe0f  Previous version ({current_version}) is now superseded (valid_until={old_valid_until})")

    print(f"\n   Next: run 'validate' to confirm no regressions.")


