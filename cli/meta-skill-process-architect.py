def cmd_network(args):
    """Build Cross-Process Network from process Skills."""
    if args.skills:
        skills_dir = Path(args.skills).resolve()
    elif args.skill and getattr(args, 'skills_dir', None):
        skills_dir = Path(args.skills_dir).resolve()
    else:
        print("\u274c Specify --skills (directory) or --skill + --skills-dir")
        return

    if not skills_dir.exists():
        print(f"\u274c Directory not found: {skills_dir}")
        return

    print("=" * 60)
    print(f"Cross-Process Network: {skills_dir.name}")
    print("=" * 60)

    # Discover all process-* directories
    process_dirs = sorted([d for d in skills_dir.iterdir()
                          if d.is_dir() and d.name.startswith("process-")])

    if not process_dirs:
        print(f"\n  \u26a0\ufe0f  No process-* directories found in {skills_dir}")
        return

    print(f"\n  Skills found: {len(process_dirs)}")
    for pd in process_dirs:
        print(f"    - {pd.name}")

    # Build network
    nodes = {}
    edges = []
    unresolved_refs = []
    missing_backlinks = []

    # First pass: index all Skills
    for pd in process_dirs:
        brief_path = pd / "references" / "process-brief.md"
        skill_md = pd / "SKILL.md"
        name = pd.name

        # Extract process name from SKILL.md title
        try:
            skill_content = skill_md.read_text()
            title_match = re.search(r'^#\s+(.+)$', skill_content, re.MULTILINE)
            display_name = title_match.group(1) if title_match else name
        except Exception:
            display_name = name

        # Read version
        version_file = pd / "VERSION"
        version = version_file.read_text().strip() if version_file.exists() else "?"

        nodes[name] = {
            "display_name": display_name,
            "version": version,
            "brief_path": brief_path,
            "edges_out": [],
            "edges_in": [],
        }

    # Second pass: extract references
    for pd in process_dirs:
        name = pd.name
        brief_path = pd / "references" / "process-brief.md"

        if not brief_path.exists():
            continue

        brief = brief_path.read_text()

        # Find L1.9 流程外 section
        out_of_scope_match = re.search(
            r'###\s+流程外\s*\n(.*?)(?=\n---|\n##|\Z)',
            brief, re.DOTALL
        )
        if not out_of_scope_match:
            # Try English
            out_of_scope_match = re.search(
                r'###\s+Out\s+of\s+Scope\s*\n(.*?)(?=\n---|\n##|\Z)',
                brief, re.DOTALL | re.IGNORECASE
            )

        if not out_of_scope_match:
            continue

        scope_text = out_of_scope_match.group(1)

        # Extract references: lines with 指向： or target:
        ref_pattern = re.compile(
            r'[-*]\s+\*\*(.+?)\*\*\s*\n.*?指向[：:]\s*(.+?)(?:\n|$)',
            re.DOTALL
        )
        for ref_match in ref_pattern.finditer(scope_text):
            ref_desc = ref_match.group(1).strip()
            ref_target = ref_match.group(2).strip()

            # Try to resolve target to a known skill
            resolved_to = None
            # Exact match
            if ref_target in nodes:
                resolved_to = ref_target
            else:
                # Fuzzy: check if any node's display_name contains the target
                for nid, ndata in nodes.items():
                    if ref_target in ndata["display_name"] or ref_target in nid:
                        resolved_to = nid
                        break
                # Fuzzy: keyword in display name
                if not resolved_to:
                    ref_keywords = ref_target.replace("流程", "").replace("Skill", "").strip()
                    for nid, ndata in nodes.items():
                        if ref_keywords and ref_keywords in ndata["display_name"]:
                            resolved_to = nid
                            break

            edge = {
                "from": name,
                "to": resolved_to or ref_target,
                "description": ref_desc,
                "resolved": resolved_to is not None,
            }
            edges.append(edge)
            nodes[name]["edges_out"].append(resolved_to or ref_target)

            if not resolved_to:
                unresolved_refs.append(ref_target)
            else:
                nodes[resolved_to]["edges_in"].append(name)

    # Check backlinks
    for edge in edges:
        if edge["resolved"]:
            # Check if target references source back
            target_node = nodes.get(edge["to"])
            if target_node and edge["from"] not in target_node["edges_out"]:
                missing_backlinks.append(f"{edge['to']} → {edge['from']}")

    # Print results
    print(f"\n  Nodes: {len(nodes)}")
    print(f"  Edges: {len(edges)}")
    print(f"  Resolved: {sum(1 for e in edges if e['resolved'])}")
    print(f"  Unresolved: {len(unresolved_refs)}")
    print(f"  Missing backlinks: {len(missing_backlinks)}")

    if unresolved_refs:
        print(f"\n  \u26a0\ufe0f  Unresolved references:")
        for ref in unresolved_refs:
            print(f"    - {ref}")

    if missing_backlinks:
        print(f"\n  \u26a0\ufe0f  Missing backlinks:")
        for bl in missing_backlinks:
            print(f"    - {bl}")
        if args.sync:
            print(f"\n  \u2139\ufe0f  --sync mode: Backlink suggestions would be generated for each missing link.")
            print(f"    Review manually or re-run with confirmation.")

    # Mermaid graph
    print(f"\n  Mermaid Graph:")
    print(f"  ```mermaid")
    print(f"  graph LR")
    for edge in edges:
        from_name = nodes[edge["from"]]["display_name"][:12]
        to_name = edge["to"] if edge["resolved"] else edge["description"][:12]
        style = "" if edge["resolved"] else "⚠️"
        print(f"    {edge['from']}[{from_name}] -->|{style}| {edge['to']}[{to_name}]")
    print(f"  ```")

    # Orphan detection
    orphans = [nid for nid, ndata in nodes.items()
               if not ndata["edges_out"] and not ndata["edges_in"]]
    if orphans:
        print(f"\n  \u26a0\ufe0f  Orphan nodes (no connections):")
        for o in orphans:
            print(f"    - {o} ({nodes[o]['display_name']})")

    # Output to file
    if args.output:
        import json as json_module
        network_map = {
            "network_name": skills_dir.name,
            "generated_at": datetime.now().isoformat(),
            "nodes": [
                {
                    "id": nid,
                    "name": ndata["display_name"],
                    "version": ndata["version"],
                    "edges_out": len(ndata["edges_out"]),
                    "edges_in": len(ndata["edges_in"]),
                }
                for nid, ndata in nodes.items()
            ],
            "edges": edges,
            "unresolved_refs": unresolved_refs,
            "missing_backlinks": missing_backlinks,
        }
        with open(args.output, 'w') as f:
            json_module.dump(network_map, f, indent=2, ensure_ascii=False)
        print(f"\n  \u2705 Network map saved: {args.output}")


