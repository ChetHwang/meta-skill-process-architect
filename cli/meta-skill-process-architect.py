def _read_skill_files(skill_path):
    """Read all text content from a skill directory."""
    content = {}
    skill_dir = Path(skill_path)
    for md_file in skill_dir.rglob("*.md"):
        try:
            content[str(md_file.relative_to(skill_dir))] = md_file.read_text()
        except Exception:
            pass
    # Also read SKILL.md if not .md
    for txt_file in skill_dir.glob("SKILL*"):
        if txt_file.suffix != ".md":
            try:
                content[str(txt_file.relative_to(skill_dir))] = txt_file.read_text()
            except Exception:
                pass
    return content


def _ap001_check(skill):
    """Check for single-char or very short role names."""
    issues = []
    for fname, content in skill.items():
        matches = re.findall(r'(?:role|角色)[：:]\s*["\']?(\S{1,2})["\']?', content, re.IGNORECASE)
        for m in matches:
            if len(m) <= 2 and m not in ["CS", "QA", "PM", "IT", "HR"] and m not in CN_BUSINESS_TERMS_ALLOWLIST:
                issues.append(f"{fname}: role name '{m}' may be too short (AP-001)")
    return issues


def _ap002_check(skill):
    """Check for person names as role names."""
    issues = []
