    wv_parser.add_argument("--at", help="Query date (YYYY-MM-DD). If omitted, shows current version info")
    wv_parser.add_argument("--list", action="store_true", help="List all versions with temporal windows")
    wv_parser.add_argument("--phase", action="store_true", help="Show only lifecycle phase for the queried date")

