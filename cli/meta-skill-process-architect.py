def cmd_ttl_check(args):
    """Scan process Skill(s) for TTL expiry."""
    if args.skills:
        skill_dirs = [Path(d).resolve() for d in args.skills.split(",")]
    elif args.skill:
        skill_dirs = [Path(args.skill).resolve()]
    else:
        print("\u274c Specify --skill or --skills")
        return

    current_quarter = _get_current_quarter()
    # Parse "YYYY-Q#" to numeric: YYYY*4 + Q - 1
    year_part = int(current_quarter[:4])
    quarter_part = int(current_quarter[6])  # "YYYY-Q#", Q# is at index 5-6
    current_q_num = year_part * 4 + quarter_part - 1

    def _parse_quarter(q_str):
        """Parse 'YYYY-Q#' to numeric quarter index."""
        parts = q_str.strip().split("-Q")
        if len(parts) != 2:
            return None
        return int(parts[0]) * 4 + int(parts[1]) - 1

    print("=" * 60)
    print(f"TTL Check Report")
    print(f"Current quarter: {current_quarter}")
    print("=" * 60)

    total_valid = 0
    total_expiring = 0
    total_expired = 0
    total_missing = 0
    total_checked = 0

    for skill_dir in skill_dirs:
        if not skill_dir.exists():
            print(f"\n\u26a0\ufe0f  Skill directory not found: {skill_dir}")
            continue

        print(f"\n\U0001f50d {skill_dir.name}")

        # Scan all .md files for @valid_until markers
        ttl_pattern = re.compile(r'@valid_until=(\d{4}-Q[1-4])')
        expired = []
        expiring = []
        valid = []

        for md_file in sorted(skill_dir.rglob("*.md")):
            try:
                content = md_file.read_text()
                for match in ttl_pattern.finditer(content):
                    q_str = match.group(1)
                    line_num = content[:match.start()].count("\n") + 1
                    # Extract surrounding context (the field name)
                    line_start = content.rfind("\n", 0, match.start()) + 1
                    line_end = content.find("\n", match.end())
                    context = content[line_start:line_end].strip()[:80]

                    q_num = _parse_quarter(q_str)
                    total_checked += 1

                    if q_num is None:
                        continue
                    elif q_num < current_q_num:
                        expired.append((md_file.relative_to(skill_dir), line_num, q_str, context))
                        total_expired += 1
                    elif q_num == current_q_num:
                        expiring.append((md_file.relative_to(skill_dir), line_num, q_str, context))
                        total_expiring += 1
                    else:
                        valid.append((md_file.relative_to(skill_dir), line_num, q_str, context))
                        total_valid += 1
            except Exception:
                pass

        if expired:
            print(f"  \u274c EXPIRED ({len(expired)}):")
            for fname, line, q, ctx in expired:
                print(f"    {fname}:{line} — {q} — {ctx}")
        if expiring:
            print(f"  \u26a0\ufe0f  EXPIRING ({len(expiring)}):")
            for fname, line, q, ctx in expiring:
                print(f"    {fname}:{line} — {q} — {ctx}")
        if valid and args.verbose:
            print(f"  \u2705 VALID ({len(valid)}):")
            for fname, line, q, ctx in valid:
                print(f"    {fname}:{line} — {q}")
        elif valid:
            print(f"  \u2705 VALID: {len(valid)} markers")
        if not expired and not expiring and not valid:
            print(f"  \u2139\ufe0f  No TTL markers found")

    # Summary
    print("\n" + "=" * 60)
    print("Summary")
    print("=" * 60)
    print(f"  Total markers checked: {total_checked}")
    print(f"  \u2705 Valid: {total_valid}")
    print(f"  \u26a0\ufe0f  Expiring: {total_expiring}")
    print(f"  \u274c Expired: {total_expired}")

    if total_expired > 0:
        print(f"\n  \u26a0\ufe0f  {total_expired} TTL markers EXPIRED. Run 'diff-plan' to assess impact.")
    elif total_expiring > 0:
        print(f"\n  \u2139\ufe0f  {total_expiring} TTL markers expiring this quarter. Schedule review.")
    else:
        print(f"\n  \u2705 All TTL markers valid.")


