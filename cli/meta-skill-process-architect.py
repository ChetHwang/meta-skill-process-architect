def cmd_validate(args):
    """Validate a generated process Skill."""
    skill_path = Path(args.skill).resolve()

    if not skill_path.exists():
        print(f"\u274c Skill path '{skill_path}' does not exist.")
        return

    print(f"\U0001f50d Validating: {skill_path}\n")

    content = _read_skill_files(skill_path)
    issues = {"block": [], "warn_confirm": [], "warn_passive": []}
    checks_passed = 0
    checks_total = 0

    # Structural completeness
    print("=" * 60)
    print("Structural Completeness")
    print("=" * 60)
    required_files = [
        "SKILL.md",
        "references/process-brief.md",
        "references/foundational-logic.md",
        "references/output-recipes.md",
    ]
    for f in required_files:
        checks_total += 1
        if f in content:
            print(f"  \u2705 {f} — present")
            checks_passed += 1
        else:
            print(f"  \u274c {f} — MISSING")
            issues["block"].append(f"Missing required file: {f}")

    # Version file
    checks_total += 1
    version_file = skill_path / "VERSION"
    if version_file.exists():
        print(f"  \u2705 VERSION — present")
        checks_passed += 1
    else:
        print(f"  \u26a0\ufe0f VERSION — MISSING (not blocking)")
        issues["warn_passive"].append("Missing VERSION file")

    # Content specificity
    print("\n" + "=" * 60)
    print("Content Specificity")
    print("=" * 60)
    vague_words = ["快速", "尽快", "around", "typically", "快速处理", "尽快回复"]
    all_text = " ".join(content.values())
    vague_found = []
    for w in vague_words:
        if w.lower() in all_text.lower():
            # Check if ALL occurrences are in whitelisted contexts
            # If at least one occurrence is NOT whitelisted, flag it
            occurrences = [m.start() for m in re.finditer(re.escape(w), all_text, re.IGNORECASE)]
            non_whitelisted = 0
            for pos in occurrences:
                context = all_text[max(0, pos-2):pos+len(w)+4]  # surrounding context
                is_whitelisted = any(wl in context for wl in VAGUE_TERM_WHITELIST)
                if not is_whitelisted:
                    non_whitelisted += 1
            if non_whitelisted > 0:
                vague_found.append(f"{w}({non_whitelisted}次非标准用法)")
    checks_total += 1
    if vague_found:
        print(f"  \u26a0\ufe0f Vague terms found: {', '.join(vague_found)}")
        issues["warn_passive"].append(f"Vague terms: {vague_found}")
    else:
        print(f"  \u2705 No vague terms — good specificity")
        checks_passed += 1

    # Role coverage (≥ 3)
    checks_total += 1
    # Match role definition patterns:
    #   1. "### 角色名（`role-id`）" — full pattern
    #   2. "- **角色名** (`role-id`)" — simplified bullet pattern  
    #   3. "### 角色名" — heading-only (legacy format)
    cn_role_pattern = r'###\s+[\u4e00-\u9fff()（）\w]+（`[\w-]+`）'
    en_role_pattern = r'###\s+[\w\s]+\(`[\w-]+`\)'
    # Generic business role suffixes — works across all industries (manufacturing, finance, tech, healthcare, education, etc.)
    bullet_role_pattern = r'[-*]\s+\*\*[\u4e00-\u9fff（）()/]{0,20}(?:经理|主管|专员|负责人|总监|工程师|分析师|操作员|代表|会计|审计|客服|审核|管理员)\*\*\s*\(?`?[\w-]+`?\)?'
    role_count_cn = len(re.findall(cn_role_pattern, all_text))
    role_count_en = len(re.findall(en_role_pattern, all_text))
    role_count_bullet = len(re.findall(bullet_role_pattern, all_text))
    # Also count "| 角色名 |" table headers in role definition tables
    table_role_count = len(re.findall(r'\|\s*[\u4e00-\u9fff]{2,6}(?:经理|主管|专员|负责人|总监|工程师|分析师|操作员|代表|会计|审计|客服|审核|管理员)\s*\|', all_text))
    role_count = max(role_count_cn, role_count_en, role_count_bullet, table_role_count)
    if role_count >= 3:
        print(f"  \u2705 Role coverage: {role_count} roles (>= 3)")
        checks_passed += 1
    else:
        print(f"  \u26a0\ufe0f Role coverage: {role_count} roles (< 3 required)")

    # Source attribution
    checks_total += 1
    if re.search(r'(?:source_attribution|方法论来源|Source Attribution)', all_text, re.IGNORECASE):
        print(f"  \u2705 Source attribution — present")
        checks_passed += 1
    else:
        print(f"  \u26a0\ufe0f Source attribution — MISSING")
        issues["warn_confirm"].append("Missing source attribution in L2")

    # Anti-pattern scan
    print("\n" + "=" * 60)
    print("Anti-Pattern Scan (10 checks)")
    print("=" * 60)
    for ap in ANTI_PATTERNS:
        checks_total += 1
        ap_issues = ap["check"](content)
        if ap_issues:
            print(f"  \u274c {ap['id']} [{ap['severity']}] {ap['name']}")
            for issue in ap_issues:
                print(f"      - {issue}")
            issues[ap["severity"]].extend(ap_issues)
        else:
            print(f"  \u2705 {ap['id']} — clean")
            checks_passed += 1

    # Summary
    print("\n" + "=" * 60)
    print("Validation Summary")
    print("=" * 60)
    print(f"  Passed: {checks_passed}/{checks_total}")

    block_count = len(issues["block"])
    warn_c_count = len(issues["warn_confirm"])
    warn_p_count = len(issues["warn_passive"])

    if block_count > 0:
        print(f"\n  \U0001f6ab BLOCK ({block_count}):")
        for i in issues["block"]:
            print(f"    - {i}")
    if warn_c_count > 0:
        print(f"\n  \u26a0\ufe0f WARN-CONFIRM ({warn_c_count}):")
        for i in issues["warn_confirm"]:
            print(f"    - {i}")
    if warn_p_count > 0:
        print(f"\n  \u2139\ufe0f WARN-PASSIVE ({warn_p_count}):")
        for i in issues["warn_passive"]:
            print(f"    - {i}")

    if block_count == 0 and warn_c_count == 0 and warn_p_count == 0:
        print(f"\n  \u2705 All checks passed! Skill is ready for packaging.")

    # Trace Mode (Phase 3)
    if getattr(args, 'trace', False):
        _run_trace_analysis(skill_path, content, args)

    # Coverage check (Phase 3)
    if getattr(args, 'coverage', False):
        _run_coverage_check(skill_path, content)

    if block_count > 0:
        print(f"\n  \u274c Validation FAILED — {block_count} blocking issues must be resolved.")
        sys.exit(1)
    elif warn_c_count > 0:
        print(f"\n  \u26a0\ufe0f Validation WARNING — {warn_c_count} issues require confirmation.")
    else:
        print(f"\n  \u2705 Validation PASSED.")

def _run_coverage_check(skill_path, content):
    """Check recipe coverage across the 7 standard roles."""
    print("\n" + "=" * 60)
    print("Coverage Check — Role Coverage")
    print("=" * 60)

    recipes_content = content.get("references/output-recipes.md", "")
    brief_content = content.get("references/process-brief.md", "")

    # Standard roles
    STANDARD_ROLES = ["operator", "auditor", "owner", "trainee", "customer", "compliance", "manager"]

    # Extract roles from process-brief
    role_pattern = re.compile(
        r'(?:###\s+([\u4e00-\u9fff()（）\w]+)（`([\w-]+)`）)|'
        r'(?:[-*]\s+\*\*([\u4e00-\u9fff]{2,8}(?:班主任|负责人|老师|主管|财务|客服|质检|销售|教务))\*\*\s*\(?`?([\w-]+)`?\)?)'
    )
    found_roles = {}
    for match in role_pattern.finditer(brief_content):
        if match.group(2):
            found_roles[match.group(2)] = match.group(1)
        elif match.group(4):
            found_roles[match.group(4)] = match.group(3)

    # Check recipe coverage per role
    role_coverage = {}
    for role_id, role_name in found_roles.items():
        # Count recipes that mention this role
        mentions = len(re.findall(re.escape(role_name), recipes_content, re.IGNORECASE))
        role_coverage[role_id] = {"name": role_name, "mentions": mentions}

    covered_roles = sum(1 for r in role_coverage.values() if r["mentions"] > 0)
    total_roles = len(role_coverage)

    print(f"  Total roles: {total_roles}")
    print(f"  Covered (mentioned in recipes): {covered_roles}")
    print(f"")

    for role_id, rc in role_coverage.items():
        status = "\u2705" if rc["mentions"] > 0 else "\u26a0\ufe0f"
        print(f"  {status} {role_id} ({rc['name']}): {rc['mentions']} recipe mentions")

    if covered_roles >= 3:
        print(f"\n  \u2705 Coverage: {covered_roles}/{total_roles} roles covered (>= 3)")
    else:
        uncovered = total_roles - covered_roles
        print(f"\n  \u274c Coverage: only {covered_roles}/{total_roles} roles covered — need >= 3")
        if uncovered > 0:
            print(f"    Recommend generating additional recipes for uncovered roles")
            for role_id, rc in role_coverage.items():
                if rc["mentions"] == 0:
                    # Suggest recipes for this role
                    if role_id in ("operator", "auditor"):
                        print(f"    → {role_id}: SOP + Runbook + FAQ")
                    elif role_id == "owner":
                        print(f"    → {role_id}: RACIO + Compliance Memo")


def _run_trace_analysis(skill_path, content, args):
    """Run Trace Mode analysis comparing documented steps vs observed execution."""
    print("\n" + "=" * 60)
    print("Trace Mode — Drift Analysis")
    print("=" * 60)

    brief_content = content.get("references/process-brief.md", "")
    if not brief_content:
        print("  \u274c process-brief.md not found — cannot run trace")
        return

    # Extract documented steps from L1.4
    step_pattern = re.compile(
        r'(?:###|####)\s+(S\d+(?:\.\d+)?(?:\.E|\.A)?)\s*[-—]\s*(.+?)(?=\n(?:###|####|\Z))',
        re.DOTALL
    )
    documented_steps = {}
    for match in step_pattern.finditer(brief_content):
        step_id = match.group(1)
        step_body = match.group(2)
        # Extract duration
        dur_match = re.search(r'(?:时长|duration)[：:\s]*["\']?(\d+)\s*(?:分钟|min|小时|hour|h|天|day)', step_body, re.IGNORECASE)
        duration = dur_match.group(1) + (dur_match.group(2) or "min") if dur_match else "unknown"
        documented_steps[step_id] = {
            "name": step_body.split("\n")[0].strip()[:60],
            "duration": duration,
        }

    print(f"  Documented steps: {len(documented_steps)}")

    # Load trace source if provided
    observed_steps = {}
    trace_source = getattr(args, 'trace_source', None)
    if trace_source:
        import csv as csv_module
        try:
            with open(trace_source, 'r') as f:
                reader = csv_module.DictReader(f)
                for row in reader:
                    # Expect: step_id, duration_minutes, status, note
                    sid = row.get('step_id', row.get('from_status', ''))
                    if sid:
                        observed_steps[sid] = {
                            "duration_min": float(row.get('duration_minutes', 0)),
                            "status": row.get('status', ''),
                            "note": row.get('note', ''),
                        }
            print(f"  Observed steps (from trace source): {len(observed_steps)}")
        except Exception as e:
            print(f"  \u26a0\ufe0f  Could not read trace source: {e}")

    # Load SLA report if provided
    sla_data = {}
    sla_report = getattr(args, 'sla_report', None)
    if sla_report:
        import csv as csv_module
        try:
            with open(sla_report, 'r') as f:
                reader = csv_module.DictReader(f)
                for row in reader:
                    sid = row.get('step_id', '')
                    if sid:
                        sla_data[sid] = {
                            "target_h": float(row.get('sla_target_hours', 0)),
                            "actual_h": float(row.get('actual_median_hours', 0)),
                            "breach_pct": float(row.get('breach_rate_pct', 0)),
                        }
            print(f"  SLA data points: {len(sla_data)}")
        except Exception as e:
            print(f"  \u26a0\ufe0f  Could not read SLA report: {e}")

    # Classification
    print("\n  Step Classification:")
    print(f"  {'Step':<10} {'Status':<12} {'Doc Duration':<15} {'Observed':<15}")
    print(f"  {'-'*10} {'-'*12} {'-'*15} {'-'*15}")

    matched = shadow = dead = drifted = unobserved = 0

    for step_id, doc_info in documented_steps.items():
        if step_id in observed_steps:
            obs = observed_steps[step_id]
            # Simple drift check: compare durations if we have numeric data
            status = "matched"
            dur_match = re.search(r'(\d+)', doc_info["duration"])
            if dur_match:
                doc_min = float(dur_match.group(1))
                obs_min = obs["duration_min"]
                if obs_min > 0:
                    delta_pct = abs(obs_min - doc_min) / doc_min * 100
                    if delta_pct > 30:
                        status = "drifted"

            if status == "matched":
                matched += 1
            else:
                drifted += 1

            print(f"  {step_id:<10} {status:<12} {doc_info['duration']:<15} {obs['duration_min']}min")
        else:
            unobserved += 1
            print(f"  {step_id:<10} unobserved   {doc_info['duration']:<15} (no data)")

    # Check for shadow steps (in observed but not documented)
    for obs_id in observed_steps:
        if obs_id not in documented_steps:
            shadow += 1
            print(f"  {obs_id:<10} shadow       {'(not in doc)':<15} {observed_steps[obs_id]['duration_min']}min")

    # SLA drift
    if sla_data:
        print(f"\n  SLA Drift:")
        for sid, sla in sla_data.items():
            if sla["breach_pct"] > 20:
                print(f"    \u26a0\ufe0f  {sid}: SLA target {sla['target_h']}h, actual {sla['actual_h']}h, breach {sla['breach_pct']}%")

    # Score
    total = len(documented_steps) + shadow
    drift_score = (shadow + dead + drifted) / max(total, 1) * 100

    print(f"\n  Trace Summary:")
    print(f"    Documented steps: {len(documented_steps)}")
    print(f"    Matched: {matched}")
    print(f"    Shadow (undocumented): {shadow}")
    print(f"    Dead (never executed): {dead}")
    print(f"    Drifted (duration/executor): {drifted}")
    print(f"    Unobserved: {unobserved}")
    print(f"    Drift Score: {drift_score:.1f}%")

    if drift_score <= 10:
        print(f"    Status: \u2705 Healthy")
    elif drift_score <= 25:
        print(f"    Status: \u26a0\ufe0f  Warning — schedule review")
    elif drift_score <= 50:
        print(f"    Status: \U0001f534 Unhealthy — escalate to process owner")
    else:
        print(f"    Status: \U0001f6a8 Critical — document unreliable, full re-extraction needed")

    # Output to file if requested
    output_file = getattr(args, 'output', None)
    if output_file:
        import yaml as yaml_module
        try:
            report = {
                "process": str(skill_path.name),
                "analysis_date": datetime.now().isoformat(),
                "summary": {
                    "baseline_steps": len(documented_steps),
                    "matched": matched,
                    "shadow_steps": shadow,
                    "dead_steps": dead,
                    "drifted_steps": drifted,
                    "unobserved": unobserved,
                    "drift_score": round(drift_score, 1),
                    "health": "healthy" if drift_score <= 10 else ("warning" if drift_score <= 25 else ("unhealthy" if drift_score <= 50 else "critical")),
                }
            }
            with open(output_file, 'w') as f:
                yaml_module.dump(report, f, default_flow_style=False, allow_unicode=True)
            print(f"\n  \u2705 Trace report saved: {output_file}")
        except ImportError:
            print(f"\n  \u26a0\ufe0f  PyYAML not installed, skipping report file")


